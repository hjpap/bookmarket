package com.wei.model;

public class PingjiaBean {
	private String pj_id;
	private String pj_content;
	private String user_id;
	private String goods_id;
	private String pj_date;
	private String pj_level;
	public String getPj_id() {
		return pj_id;
	}
	public void setPj_id(String pjId) {
		pj_id = pjId;
	}
	public String getPj_content() {
		return pj_content;
	}
	public void setPj_content(String pjContent) {
		pj_content = pjContent;
	}
	public String getUser_id() {
		return user_id;
	}
	public void setUser_id(String userId) {
		user_id = userId;
	}
	public String getGoods_id() {
		return goods_id;
	}
	public void setGoods_id(String goodsId) {
		goods_id = goodsId;
	}
	public String getPj_date() {
		return pj_date;
	}
	public void setPj_date(String pjDate) {
		pj_date = pjDate;
	}
	public String getPj_level() {
		return pj_level;
	}
	public void setPj_level(String pjLevel) {
		pj_level = pjLevel;
	}
	
	
}
